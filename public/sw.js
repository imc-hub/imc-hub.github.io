importScripts('https://storage.googleapis.com/workbox-cdn/releases/7.3.0/workbox-sw.js');

workbox.setConfig({ debug: false });
workbox.loadModule('workbox-strategies');
workbox.loadModule('workbox-precaching');
workbox.loadModule('workbox-routing');
workbox.loadModule('workbox-cacheable-response');
workbox.loadModule('workbox-expiration');

const { clientsClaim } = workbox.core;
const { precacheAndRoute, createHandlerBoundToURL } = workbox.precaching;
const { registerRoute, Route } = workbox.routing;
const { NetworkFirst, CacheFirst, StaleWhileRevalidate } = workbox.strategies;
const { ExpirationPlugin } = workbox.expiration;
const { CacheableResponsePlugin } = workbox.cacheableResponse;

// Skip waiting and claim all clients immediately
self.skipWaiting();
clientsClaim();

// Listen for messages from the page
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
});

// Precache all static assets generated by Next.js build
// Revision bumps force cache refresh when content changes
precacheAndRoute([
  { url: '/', revision: '3' },
  { url: '/about', revision: '3' },
  { url: '/academy', revision: '3' },
  { url: '/digital-solutions', revision: '2' },
  { url: '/assessment', revision: '3' },
  { url: '/contact', revision: '3' },
  { url: '/privacy', revision: '3' },
  { url: '/terms', revision: '3' },
  { url: '/cookies', revision: '3' },
]);

// Navigation requests — NetworkFirst with short TTL for freshness
// Pages revalidate on every navigation, serving stale only if offline
const navigationRoute = new Route(({ request }) => {
  return request.mode === 'navigate';
}, new NetworkFirst({
  cacheName: 'pages-cache',
  plugins: [
    new CacheableResponsePlugin({ statuses: [200] }),
    new ExpirationPlugin({ maxEntries: 20, maxAgeSeconds: 300 }),
  ],
  networkTimeoutSeconds: 3,
}));

// Offline fallback — serve cached index for navigation failures
const { setCatchHandler } = workbox.routing;
setCatchHandler(({ event }) => {
  if (event.request.destination === 'document') {
    return caches.match('/');
  }
  return Response.error();
});

registerRoute(navigationRoute);

// CSS/JS bundles — StaleWhileRevalidate (hashed filenames = immutable, but check for updates)
registerRoute(
  ({ request }) =>
    request.destination === 'style' || request.destination === 'script',
  new StaleWhileRevalidate({
    cacheName: 'static-resources',
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 100, maxAgeSeconds: 86400 }),
    ],
  })
);

// Image assets — CacheFirst but with shorter expiry
registerRoute(
  ({ request }) => request.destination === 'image',
  new CacheFirst({
    cacheName: 'images-cache',
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 60, maxAgeSeconds: 2592000 }),
    ],
  })
);

// Google Fonts stylesheets — StaleWhileRevalidate
registerRoute(
  ({ url }) => url.origin === 'https://fonts.googleapis.com',
  new StaleWhileRevalidate({
    cacheName: 'google-fonts-stylesheets',
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 10, maxAgeSeconds: 604800 }),
    ],
  })
);

// Google Fonts files — CacheFirst (versioned URLs)
registerRoute(
  ({ url }) => url.origin === 'https://fonts.gstatic.com',
  new CacheFirst({
    cacheName: 'google-fonts-webfonts',
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 30, maxAgeSeconds: 31536000 }),
    ],
  })
);

// EmailJS API — NetworkOnly (never cache email submissions)
registerRoute(
  ({ url }) => url.origin.includes('emailjs'),
  new workbox.strategies.NetworkOnly()
);
