importScripts(
  "https://storage.googleapis.com/workbox-cdn/releases/7.3.0/workbox-sw.js",
);

workbox.setConfig({ debug: false });
workbox.loadModule("workbox-strategies");
workbox.loadModule("workbox-precaching");
workbox.loadModule("workbox-routing");
workbox.loadModule("workbox-cacheable-response");
workbox.loadModule("workbox-expiration");

const { clientsClaim } = workbox.core;
const { precacheAndRoute } = workbox.precaching;
const { registerRoute } = workbox.routing;
const { StaleWhileRevalidate, CacheFirst, NetworkOnly } = workbox.strategies;
const { ExpirationPlugin } = workbox.expiration;
const { CacheableResponsePlugin } = workbox.cacheableResponse;

self.skipWaiting();
clientsClaim();

// Listen for messages from the page
self.addEventListener("message", (event) => {
  if (event.data && event.data.type === "SKIP_WAITING") {
    self.skipWaiting();
  }
});

// Precache all static assets generated by Next.js build.
// These are the HTML pages and their dependencies.
// Revision bumps force cache refresh when content changes.
precacheAndRoute([
  { url: "/", revision: "3" },
  { url: "/about.html", revision: "3" },
  { url: "/academy.html", revision: "3" },
  { url: "/assessment.html", revision: "3" },
  { url: "/contact.html", revision: "3" },
  { url: "/cookies.html", revision: "3" },
  { url: "/digital-solutions.html", revision: "3" },
  { url: "/digital-solutions/rx-challenger.html", revision: "3" },
  { url: "/faq.html", revision: "3" },
  { url: "/privacy.html", revision: "3" },
  { url: "/terms.html", revision: "3" },
]);

// Navigation requests — use NetworkOnly to avoid stale fallbacks.
// This site is fully static on GitHub Pages; there's no dynamic content
// that benefits from caching navigation. NetworkOnly ensures the browser
// always gets a fresh response from the server, and a real 404/500
// bubbles up correctly instead of being masked by a stale fallback.
registerRoute(({ request }) => request.mode === "navigate", new NetworkOnly());

// CSS/JS bundles — StaleWhileRevalidate (hashed filenames = immutable, but check for updates)
registerRoute(
  ({ request }) =>
    request.destination === "style" || request.destination === "script",
  new StaleWhileRevalidate({
    cacheName: "static-resources",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 100, maxAgeSeconds: 86400 }),
    ],
  }),
);

// Image assets — CacheFirst but with shorter expiry
registerRoute(
  ({ request }) => request.destination === "image",
  new CacheFirst({
    cacheName: "images-cache",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 60, maxAgeSeconds: 2592000 }),
    ],
  }),
);

// Google Fonts stylesheets — StaleWhileRevalidate
registerRoute(
  ({ url }) => url.origin === "https://fonts.googleapis.com",
  new StaleWhileRevalidate({
    cacheName: "google-fonts-stylesheets",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 10, maxAgeSeconds: 604800 }),
    ],
  }),
);

// Google Fonts files — CacheFirst (versioned URLs)
registerRoute(
  ({ url }) => url.origin === "https://fonts.gstatic.com",
  new CacheFirst({
    cacheName: "google-fonts-webfonts",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 30, maxAgeSeconds: 31536000 }),
    ],
  }),
);

// EmailJS API — NetworkOnly (never cache email submissions)
registerRoute(({ url }) => url.origin.includes("emailjs"), new NetworkOnly());
