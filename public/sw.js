importScripts(
  "https://storage.googleapis.com/workbox-cdn/releases/7.3.0/workbox-sw.js",
);

workbox.setConfig({ debug: false });
workbox.loadModule("workbox-strategies");
workbox.loadModule("workbox-precaching");
workbox.loadModule("workbox-routing");
workbox.loadModule("workbox-cacheable-response");
workbox.loadModule("workbox-expiration");

const { clientsClaim } = workbox.core;
const { precacheAndRoute, cleanupOutdatedCaches } = workbox.precaching;
const { registerRoute } = workbox.routing;
const { NetworkFirst, StaleWhileRevalidate, CacheFirst } = workbox.strategies;
const { ExpirationPlugin } = workbox.expiration;
const { CacheableResponsePlugin } = workbox.cacheableResponse;

self.skipWaiting();
clientsClaim();

// Clean up old caches from previous SW versions immediately on activate.
// This ensures stale CSS/JS from old builds are purged.
cleanupOutdatedCaches();

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((cacheNames) =>
        Promise.all(
          cacheNames
            .filter(
              (name) =>
                name.startsWith("workbox-") ||
                name.startsWith("images-") ||
                name.startsWith("google-"),
            )
            .map((name) => caches.delete(name)),
        ),
      ),
  );
});

// Listen for messages from the page
self.addEventListener("message", (event) => {
  if (event.data && event.data.type === "SKIP_WAITING") {
    self.skipWaiting();
  }
});

// Precache all static assets generated by Next.js build.
// Revision bumps force cache refresh when content changes.
precacheAndRoute([
  { url: "/", revision: "4" },
  { url: "/about.html", revision: "4" },
  { url: "/academy.html", revision: "4" },
  { url: "/assessment.html", revision: "4" },
  { url: "/contact.html", revision: "4" },
  { url: "/cookies.html", revision: "4" },
  { url: "/digital-solutions.html", revision: "4" },
  { url: "/digital-solutions/rx-challenger.html", revision: "4" },
  { url: "/faq.html", revision: "4" },
  { url: "/privacy.html", revision: "4" },
  { url: "/terms.html", revision: "4" },
]);

// Navigation requests — NetworkFirst with short timeout.
// Falls back to precached HTML only if network is completely unreachable.
registerRoute(
  ({ request }) => request.mode === "navigate",
  new NetworkFirst({
    cacheName: "html-pages",
    networkTimeoutSeconds: 3,
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 20, maxAgeSeconds: 3600 }),
    ],
  }),
);

// CSS/JS bundles — NetworkFirst with short timeout.
// Hashed filenames change every build, so NetworkFirst ensures fresh assets.
// StaleWhileRevalidate was serving old CSS when revalidation failed silently.
registerRoute(
  ({ request }) =>
    request.destination === "style" || request.destination === "script",
  new NetworkFirst({
    cacheName: "static-resources",
    networkTimeoutSeconds: 3,
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 100, maxAgeSeconds: 86400 }),
    ],
  }),
);

// Image assets — CacheFirst but with shorter expiry
registerRoute(
  ({ request }) => request.destination === "image",
  new CacheFirst({
    cacheName: "images-cache",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 60, maxAgeSeconds: 2592000 }),
    ],
  }),
);

// Google Fonts stylesheets — StaleWhileRevalidate
registerRoute(
  ({ url }) => url.origin === "https://fonts.googleapis.com",
  new StaleWhileRevalidate({
    cacheName: "google-fonts-stylesheets",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 10, maxAgeSeconds: 604800 }),
    ],
  }),
);

// Google Fonts files — CacheFirst (versioned URLs)
registerRoute(
  ({ url }) => url.origin === "https://fonts.gstatic.com",
  new CacheFirst({
    cacheName: "google-fonts-webfonts",
    plugins: [
      new CacheableResponsePlugin({ statuses: [200] }),
      new ExpirationPlugin({ maxEntries: 30, maxAgeSeconds: 31536000 }),
    ],
  }),
);

// EmailJS API — NetworkOnly (never cache email submissions)
registerRoute(({ url }) => url.origin.includes("emailjs"), new NetworkOnly());
